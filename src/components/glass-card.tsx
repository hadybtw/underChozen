"use client";

import { cn } from "@/lib/utils";
import { motion, type HTMLMotionProps } from "framer-motion";

interface GlassCardProps extends HTMLMotionProps<"div"> {
  glow?: boolean;
}

export function GlassCard({
  className,
  glow,
  children,
  ...props
}: GlassCardProps) {
  return (
    <motion.div
      className={cn(
        "glass rounded-2xl p-6",
        glow && "glow-accent",
        className
      )}
      {...props}
    >
      {children}
    </motion.div>
  );
}
